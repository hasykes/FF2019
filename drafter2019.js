const currentPositions = { RB: 1, WR: 1, TE: 0, QB: 0, K: 0, DST: 0 };

const myDraftedPlayers = ["Christian McCaffrey", "JuJu Smith-Schuster"];
const theirDraftedPlayers = [];
const whoPickedStack = [];

//functions
function filterRemainingPlayers() {
  const draftedPlayers = [...myDraftedPlayers, ...theirDraftedPlayers];

  return draftRanks.filter(p => {
    if (draftedPlayers.indexOf(p.Name) == -1) {
      if (currentPositions[p.Pos] < draftStrategy[p.Pos]) {
        return p.Name;
      }
    }
  });
}

//I really shouldn't have this but I do...
function dumbFilter() {
  const draftedPlayers = [...myDraftedPlayers, ...theirDraftedPlayers];
  return draftRanks.filter(p => {
    if (draftedPlayers.indexOf(p.Name) == -1) {
      return p.Name;
    }
  });
}

function reducePlayerPositions(playerName) {
  draftRanks.forEach(p => {
    if (p.Name === playerName) {
      currentPositions[p.Pos] -= 1;
    }
  });
}

function addToMyPlayers() {
  const remainingPlayers = filterRemainingPlayers();
  myDraftedPlayers.push(remainingPlayers[0].Name);
  currentPositions[remainingPlayers[0].Pos] += 1;
  whoPickedStack.push("Me");
  renderUI();
}

function addToTheirPlayers() {
  const remainingPlayers = filterRemainingPlayers();
  theirDraftedPlayers.push(remainingPlayers[0].Name);
  whoPickedStack.push("Them");
  renderUI();
}

function undo() {
  console.log(whoPickedStack[whoPickedStack.length - 1]);
  switch (whoPickedStack[whoPickedStack.length - 1]) {
    case "Me":
      reducePlayerPositions(myDraftedPlayers.pop());
      break;
    case "Them":
      theirDraftedPlayers.pop();
      break;
    case undefined:
      alert("There's nothing to undo");
      break;
    default:
      alert("I don't know what you did, but it was wrong.");
  }

  whoPickedStack.pop();
  renderUI();
}

//render function
function renderUI() {
  let remainingPlayers = filterRemainingPlayers();

  if (remainingPlayers.length === 0) {
    remainingPlayers = dumbFilter();
  }

  playerToSelectDisplay.innerText = remainingPlayers[0].Name;

  playersSelectedList.forEach((listItem, i) => {
    if (myDraftedPlayers[i]) {
      listItem.innerText = myDraftedPlayers[i];
    } else {
      listItem.innerText = "";
    }
  });
}

